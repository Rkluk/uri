qt = int(input())
for x in range(qt):
    dic = {}
    player1, play1, player2, play2 = input().split()
    num1, num2 = [int(x) for x in input().split()]
    dic[play1] = player1
    dic[play2] = player2
    evenodd = 'even' if (num1 + num2) % 2 == 0 else 'odd'
    if evenodd == 'even':
        print(dic['PAR'])
    else:
        print(dic['IMPAR'])
